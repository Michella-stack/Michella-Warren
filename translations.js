// ── Language switcher logic ──────────────────────────────
const LANG_LABELS = { en: "EN", es: "ES", fr: "FR", ar: "عر" };
const LANG_NAMES  = { en: "English", es: "Español", fr: "Français", ar: "العربية" };

function getLang() {
  return localStorage.getItem('mvw_lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('mvw_lang', lang);
  applyLang(lang);
}

function applyLang(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS.en;

  // RTL support for Arabic
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  document.body.classList.toggle('rtl', lang === 'ar');

  // Apply all translations
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  // Apply HTML translations (for elements with HTML content)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // Update switcher button label
  const btn = document.getElementById('lang-current');
  if (btn) btn.textContent = LANG_LABELS[lang] || lang.toUpperCase();

  // Mark active in dropdown
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.getAttribute('data-lang') === lang);
  });
}

function buildSwitcher() {
  const switcher = document.createElement('div');
  switcher.id = 'lang-switcher';
  switcher.innerHTML = `
    <button id="lang-toggle" aria-label="Change language">
      <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="10"/><line x1="2" y1="12" x2="22" y2="12"/><path d="M12 2a15.3 15.3 0 0 1 4 10 15.3 15.3 0 0 1-4 10 15.3 15.3 0 0 1-4-10 15.3 15.3 0 0 1 4-10z"/></svg>
      <span id="lang-current">EN</span>
    </button>
    <div id="lang-dropdown">
      ${Object.entries(LANG_NAMES).map(([code, name]) =>
        `<button class="lang-option" data-lang="${code}">${name}</button>`
      ).join('')}
    </div>
  `;
  document.body.appendChild(switcher);

  document.getElementById('lang-toggle').addEventListener('click', e => {
    e.stopPropagation();
    switcher.classList.toggle('open');
  });
  document.addEventListener('click', () => switcher.classList.remove('open'));
  switcher.querySelectorAll('.lang-option').forEach(btn => {
    btn.addEventListener('click', e => {
      e.stopPropagation();
      setLang(btn.getAttribute('data-lang'));
      switcher.classList.remove('open');
    });
  });
}

document.addEventListener('DOMContentLoaded', () => {
  buildSwitcher();
  applyLang(getLang());
});
